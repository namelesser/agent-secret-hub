@app.command()
def export(
    output: Annotated[str, typer.Option("--output", "-o", help="导出文件路径，默认 stdout")] = "",
) -> None:
    """导出本设备可用的所有凭证为 JSON"""
    response = request("GET", "/secrets", headers=auth_headers())
    data = response.json()
    content = json.dumps(data, indent=2, ensure_ascii=False)
    if output:
        Path(output).write_text(content, encoding="utf-8")
        typer.echo(f"已导出 {len(data)} 条凭证到 {output}")
    else:
        typer.echo(content)


@app.command("import")
def import_secrets(
    file: Annotated[str, typer.Argument(help="JSON 文件路径")],
) -> None:
    """从 JSON 文件批量导入凭证"""
    path = Path(file)
    if not path.exists():
        typer.echo(f"文件不存在：{file}", err=True)
        raise typer.Exit(1)
    data = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(data, list):
        typer.echo("JSON 文件格式错误，需要数组格式", err=True)
        raise typer.Exit(1)
    count = 0
    for s in data:
        request("POST", "/secrets", json={"name": s["name"], "type": s.get("type", "generic"), "data": s["data"]}, headers=auth_headers())
        count += 1
        typer.echo(f"  导入 {s['name']}")
    typer.echo(f"\n共导入 {count} 条凭证")


@app.command()
def update() -> None:
    """更新客户端到最新版本"""
    repo = Path(__file__).resolve().parents[1]
    if not (repo / ".git").exists():
        typer.echo("错误：非 git 仓库，无法更新", err=True)
        raise typer.Exit(1)
    
    typer.echo("正在更新客户端...")
    commands = [
        ["git", "-C", str(repo), "fetch", "origin", "main"],
        ["git", "-C", str(repo), "checkout", "main"],
        ["git", "-C", str(repo), "reset", "--hard", "origin/main"],
        [sys.executable, "-m", "pip", "install", "-e", str(repo)],
    ]
    
    for cmd in commands:
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
        if result.returncode != 0:
            typer.echo(f"更新失败：{result.stderr}", err=True)
            raise typer.Exit(1)
    
    typer.echo("✓ 客户端已更新到最新版本")



if __name__ == "__main__":
    auto_update_client()
    app()
